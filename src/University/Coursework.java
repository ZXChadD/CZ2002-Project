package University;

public class Coursework
{
	private double percentage;
	private int max;
	
	public Coursework(double percentage, int max)
	{
		this.percentage = percentage;
		this. max = max;
	}
	
	public double getPercentage()
	{
		return percentage;
	}
	
	public int getMax()
	{
		return max;
	}
}
